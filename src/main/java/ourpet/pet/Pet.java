package ourpet.pet;

public class Pet {
	private String p_code;
	private String p_id;
	private String p_name;
	private String p_gender;
	private String p_birth;
	private String p_species;
	private String p_breed;
	private String p_weight;
	
	public String getP_code() {
		return p_code;
	}
	public void setP_code(String p_code) {
		this.p_code = p_code;
	}
	public String getP_id() {
		return p_id;
	}
	public void setP_id(String p_id) {
		this.p_id = p_id;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_gender() {
		return p_gender;
	}
	public void setP_gender(String p_gender) {
		this.p_gender = p_gender;
	}
	public String getP_birth() {
		return p_birth;
	}
	public void setP_birth(String p_birth) {
		this.p_birth = p_birth;
	}
	public String getP_species() {
		return p_species;
	}
	public void setP_species(String p_species) {
		this.p_species = p_species;
	}
	public String getP_breed() {
		return p_breed;
	}
	public void setP_breed(String p_breed) {
		this.p_breed = p_breed;
	}
	public String getP_weight() {
		return p_weight;
	}
	public void setP_weight(String p_weight) {
		this.p_weight = p_weight;
	}
	
}
